package dev.xelond;

public class Math {

    public static float psquare(float a) {
        return a + a;
    }
    public static float prectangle(float a, float b, float c) {
        return a + b + c;
    }
    public static float psosixyi(float a, float b) {
        return a + b;
    }

    public static float square(float a) {
        return a * a;
    }
    public static float rectangle(float a, float h) {
        return (a * h) / 2;
    }
    public static float sosixyi(float a, float b) {
        return a * b;
    }

}
